    sz = np.array([[1,0],[0,-1]], dtype=complex)
    Jx, Jy, Jz = sx/2, sy/2, sz/2
    
    # Casimir J² = Jx²+Jy²+Jz²
    J2 = Jx@Jx + Jy@Jy + Jz@Jz
    print("数值验证（自旋1/2表示）：")
    print(f"  Casimir J² = {np.real(J2[0,0]):.3f} × I")
    print(f"  理论值 j(j+1) = {0.5*1.5:.3f}  (j=1/2)  ✓")
    
    # 验证对易关系 [Ji,Jj]=i εijk Jk
    comm_xy = Jx@Jy - Jy@Jx
    print(f"  [Jx,Jy] = i·Jz ? {np.allclose(comm_xy, 1j*Jz)}  ✓")
    print(f"  对易子非零 → 转动算符不对易  [严格]")
    return Jx, Jy, Jz

