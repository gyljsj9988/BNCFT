# ============================================================
# 核心：720°双值覆盖的拓扑结测量
# ============================================================

def test_double_cover():
    """
    [物理] 拓扑结的'指向'是一个旋量(spinor)，720°复原。
    
    数学表示：
      普通矢量在角度α的投影 = cos(α)
      旋量(720°复原)在角度α的'投影' = cos(α/2)  ← 半角！
    
    但测量时观测的是振幅，振幅平方对应概率/强度。
    """
    print("=" * 70)
    print("验证：720°双值覆盖拓扑结的关联")
    print("=" * 70)
    
    n = 2000000
    
    def correlation_spinor(theta_a, theta_b):
        """
        [物理] 共享背景场给两个拓扑结一个共同的'旋量相位' φ
        [物理] φ 在双值覆盖空间均匀分布 → [0, 4π)
        [物理] 拓扑结A是旋量，在测量方向θ_a的投影涉及半角
        [物理] 测量读取连续场振幅（旋量分量）
        """
        # 共享旋量相位（双值覆盖：0到4π）
        phi = np.random.uniform(0, 4*np.pi, n)
        
        # [物理] 旋量在测量方向的投影 = cos((φ - 2θ)/2)
        # 关键：测量装置角度θ以"2θ"进入（因为旋量旋转θ对应物理旋转2θ）
        # 这是SU(2)->SO(3)的2:1映射
        proj_A = np.cos((phi - 2*theta_a)/2)
        proj_B = np.cos((phi - 2*theta_b)/2)
        
        # [物理] 反向关联（Bell单态：两旋量反向）
        # 连续投影乘积
        E = np.mean(proj_A * proj_B) * 2  # 归一化
        return E
    
    print("\n[检验] E(0,θ) 旋量投影 vs 目标cos(2θ):")
    print(f"{'θ(度)':>8} | {'旋量投影':>10} | {'cos(2θ)':>10} | {'偏差':>10}")
    print("-" * 48)
    
    for deg in [0, 22.5, 45, 67.5, 90, 112.5, 135, 180]:
        theta = math.radians(deg)
        E = correlation_spinor(0, theta)
        target = math.cos(2*theta)
        print(f"{deg:>8.1f} | {E:>+10.4f} | {target:>+10.4f} | {E-target:>+10.4f}")
    
    return correlation_spinor


# ============================================================
# CHSH计算
# ============================================================

def compute_chsh_spinor(corr_func):
    """用旋量模型算CHSH"""
    print("\n" + "=" * 70)
    print("CHSH计算")
    print("=" * 70)
    
    a, a2 = 0, math.pi/4
    b, b2 = math.pi/8, 3*math.pi/8
    
    E_ab = corr_func(a, b)
    E_ab2 = corr_func(a, b2)
    E_a2b = corr_func(a2, b)
    E_a2b2 = corr_func(a2, b2)
    
    print(f"\n  E(0°,  22.5°) = {E_ab:+.4f}")
    print(f"  E(0°,  67.5°) = {E_ab2:+.4f}")
    print(f"  E(45°, 22.5°) = {E_a2b:+.4f}")
    print(f"  E(45°, 67.5°) = {E_a2b2:+.4f}")
    
    S = abs(E_ab - E_ab2 + E_a2b + E_a2b2)
    
    print(f"\n  CHSH S = {S:.4f}")
    print(f"  经典界限 = 2.0")
    print(f"  量子界限 = {2*math.sqrt(2):.4f}")
    print(f"  违背经典: {'是 ✓' if S > 2.0 else '否'}")
    print(f"  达到量子极限: {'是 ✓' if abs(S - 2*math.sqrt(2)) < 0.05 else '否'}")
    
    return S


