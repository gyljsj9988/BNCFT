    n = 2000000
    phi = np.random.uniform(0, 4*np.pi, n)
    
    print("方案A：测量角度直接进入半角 cos((φ-θ)/2)")
    def corr_A(ta, tb):
        pA = np.cos((phi - ta)/2)
        pB = np.cos((phi - tb)/2)
        return np.mean(pA*pB)*2
    for deg in [0, 45, 90, 180]:
        t = math.radians(deg)
        print(f"  θ={deg:>5.1f}°: E={corr_A(0,t):+.4f}, cos(θ/2)={math.cos(t/2):+.4f}")
    
    print("\n方案B：测量角度加倍进入半角 cos((φ-2θ)/2)=cos(φ/2-θ)")
